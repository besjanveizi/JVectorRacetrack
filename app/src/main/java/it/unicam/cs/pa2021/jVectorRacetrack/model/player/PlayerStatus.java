package it.unicam.cs.pa2021.jVectorRacetrack.model.player;

/**
 * Represents the <code>Player</code> status
 */
public enum PlayerStatus {
    /**
     * The <code>Player</code> is currently playing.
     */
    PLAYING,
    /**
     * The <code>Player</code> has finished the race.
     */
    FINISHED,
    /**
     * The <code>Player</code> has crashed.
     */
    CRASHED
}
